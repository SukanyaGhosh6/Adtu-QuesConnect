package com.adtu.quesconnect.model;

public class ModelC {
    String k,t,x,des;

    public ModelC() {
    }

    public ModelC(String k, String t, String x, String des) {
        this.k = k;
        this.t = t;
        this.x = x;
        this.des = des;
    }

    public String getK() {
        return k;
    }

    public void setK(String k) {
        this.k = k;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }
}
